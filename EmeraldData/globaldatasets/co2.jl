"""

    CO₂_pressure(year::Int)

Return the CO₂ pressure in Pa, given
- `year` Year

"""
function CO₂_pressure end;

CO₂_pressure(year::Int) = CCS_1Y.MEAN[findfirst(CCS_1Y.YEAR .== year)];
